      <p class="pb-6 mt-10">
      Multiple unit apartment buildings or rental apartments have a big
        impact in the environment. They produce more waste than own use
        properties. While zero waste seems and daunting goal for rentals and
        with limitations depending on the rules and restrictions set by the
        property owner or management, there are still many steps you can
        take to minimize waste and make environmentally conscious choices.
        Here are some suggestions:</p>

      <ol class="list-decimal">
        <li class="pb-6 mt-10 font-bold">Reduce single-use items:</li>
        <p>Avoid purchasing single-use items like
        plastic water bottles, disposable cutlery, and paper towels. Instead,
        opt for reusable alternatives like a refillable water bottle, stainless
        steel cutlery, and cloth towels.
        </p>

        <li class="pb-6 mt-10 font-bold">Practice recycling:</li>
        <p>Familiarize yourself with the recycling
        guidelines in your area and make sure to separate recyclable
        materials properly. Check if your rental property has recycling
        facilities or designated bins, and if not, request permission to set up
        your own recycling system.
        </p>

        <x-enrollment-ad>
        </x-enrollment-ad>

        <li class="pb-6 mt-10 font-bold">Composting:</li>
        <p>If you have outdoor space or access to a communal
        garden, consider starting a composting system. Composting allows
        you to divert organic waste from the landfill and create nutrient-rich
        soil for plants. There are various composting methods available,
        including traditional compost bins, worm composting, or bokashi
        composting. 
        </p>

        


        <li class="pb-6 mt-10 font-bold">Mindful shopping:</li>
        <p class="mb-10">Choose products with minimal packaging and
        opt for bulk items when possible. Bring your own reusable eco bags,
        produce bags, and containers to reduce plastic waste. Buy second-
        hand items or borrow from neighbors or friends instead of purchasing
        new items especially if you will need to use them once or when
        needed. Those items that we buy for the purpose of having them
        around when we need them usually become clutter.
        </p>


        
        <li class="pb-6 mt-10 font-bold">Donate and recycle:</li>
        <p>When you no longer need certain items,
        donate them to local charities or thrift stores. Electronics, batteries,
        and other items that require specialized recycling need to be
        segregated as e-waste and disposed of properly by accredited e-
        waste recycling facilities.
        </p>

        <li class="pb-6 mt-10 font-bold">Energy efficiency:</li>
        <p>Take steps to conserve energy in your rental
        property. Use energy-efficient light bulbs, turn off lights when not in
        use, unplug electronics when they&#39;re not being used, and regulate
        the thermostat to reduce unnecessary heating or cooling.
        </p>

        <li class="pb-6 mt-10 font-bold">Conscious cleaning:</li>
        <p>Choose eco-friendly cleaning products or
        make your own using natural ingredients like vinegar, baking soda,
        and essential oils. Avoid using harsh chemicals that can be harmful
        to the environment. 
        </p>

        
