      <p class="pb-6 mt-10">
        Electricity consumption during the summer tend to increase due to the frequent and longer use of air condition units
        and other cooling appliances. Reducing energy consumption in a building especially a rental property is crucial for a
        sustainable operations. Lower utility bills benefit the environment, and potentially attract energy-conscious tenants.
        Here are some practical steps you can take to achieve energy efficiency:
      </p>

      <ol class="list-decimal">
        <li class="pb-6 mt-10 font-bold">Replace traditional light bulbs with energy-efficient alternatives:</li>
        <p>Swap out incandescent bulbs with compact fluorescent lamps (CFLs) or light-emitting diodes (LEDs). These options
        consume less energy and have a longer lifespan. LED bulbs are not only affordable but they also come in warm,
        warm white, and day light to achieve the room ambiance that you want to achieve.
        </p>

        <li class="pb-6 mt-10 font-bold">Install programmable thermostats:</li>
        <p>This is a basic feature of houses in four season climates. Programmable
        thermostats allow you to automatically adjust the temperature settings based on occupancy patterns. Set it to reduce
        heating or cooling when the property is unoccupied or during nighttime hours. This needs to be included in the
        building plans or consult your electrical engineer how to add this feature.
        </p>

        <x-enrollment-ad>
        </x-enrollment-ad>

        <li class="pb-6 mt-10 font-bold">Seal air leaks:</li>
        <p>Identify and seal any gaps or cracks around windows, doors, and other openings to prevent drafts.
        Use weatherstripping, caulking, or door sweeps to close these gaps and improve insulation. This is part of regular
        maintenance program. You will need to reseal again after a year. Best time to reseal is during summer before the
        rainy season. Sealing air leaks have dual purpose, to keep cool air from leasing thereby reducing energy
        consumption and to keep the rain water from entering through the small gaps.
        </p>

        


        <li class="pb-6 mt-10 font-bold">Maintain the Property Regularly</li>
        <p class="mb-10">Regular maintenance is essential to keep your rental property in good condition and avoid costly repairs. Schedule regular inspections to check for any issues that need attention. Address any maintenance requests promptly to keep your tenants happy and avoid potential legal issues.
        </p>

    

        
        <li class="pb-6 mt-10 font-bold">Insulate properly:</li>
        <p>Ensure that the rental property has adequate insulation in the walls, attic, and floors.
        Insulation helps maintain a comfortable indoor temperature and reduces the need for excessive heating or cooling.
        </p>

        <li class="pb-6 mt-10 font-bold">Encourage energy-efficient habits:</li>
        <p>Educate tenants on energy-saving practices such as turning off lights when
        not in use, unplugging electronics when not needed, and using appliances efficiently. Provide resources or
        informational materials to promote energy-conscious behavior. Strategically put these reminders near outlets,
        switches and electric appliances for maximum effect.
        </p>

        <x-ad-section-template>
        </x-ad-section-template>

        <li class="pb-6 mt-10 font-bold">Opt for energy-efficient appliances:</li>
        <p>When replacing appliances, choose models with high energy efficiency
        ratings (e.g., Energy Star certified). This applies to refrigerators, dishwashers, washing machines, and other
        appliances commonly found in rental properties. Replace old non- inverter refrigerators and air conditioners with
        inverter models because they consume 45% less electricity according to experts.
        </p>

        <li class="pb-6 mt-10 font-bold">Monitor and maintain HVAC systems::</li>
        <p>Regularly inspect and maintain heating, ventilation, and air conditioning
        (HVAC) systems. Dirty filters, leaky ducts, or faulty equipment can increase energy consumption. Encourage tenants
        to report any HVAC issues promptly.
        </p>

        <li class="pb-6 mt-10 font-bold">Install window treatments:</li>
        <p>Implement window coverings such as blinds, curtains, or window films to reduce heat
        gain during summers and heat loss during winters. This helps maintain a more comfortable indoor temperature
        without relying heavily on heating or cooling systems. 
        </p>

        <li class="pb-6 mt-10 font-bold">Conduct energy audits:</li>
        <p>Consider conducting professional energy audits periodically to assess the property&#39;s
        energy efficiency. These audits can identify areas for improvement and provide recommendations tailored to the
        rental property.
        </p>

